    private void btn_hapusdaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_hapusdaMouseClicked
        if(txt_usernameda.getText().equals("") || txt_passwordda.getText().equals("") || txt_namada.getText().equals("") || txt_jabatanda.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Masih Belum Memilih Data Atau Ada Form Yang Kosong");
        }
        else{
            try{
                Akun.hapusAkun(Id_Akun);
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                Akun.show_akun(tbl_da);
                clearAkun();
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_btn_hapusdaMouseClicked

